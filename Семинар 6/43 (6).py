# print ("Введите элементы массива (целые числа), разделив их пробелами:")
# array_str = input()
array_str = "3 1 2 5 4 6"
array = array_str.split()
array = list(map(int, array))
print ("исходный список:", array)

array1 = sorted(filter(lambda x: x%2 == 0, array), reverse=True)    # четные
array2 = sorted(filter(lambda x: x%2 == 1, array))                  # нечетные
array3 = array

for indx, value in enumerate (array):
    if value%2 == 0:
        array3[indx] = array1.pop(0)
    else:
        array3[indx] = array2.pop(0)
print ("отсортированный список:", array3)